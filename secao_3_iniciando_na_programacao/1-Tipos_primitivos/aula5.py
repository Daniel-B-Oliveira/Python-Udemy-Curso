# Tipo de dado bool (bolean)
# Ao questionar algo em um programa
# Só existem duas repostar possíveis
# Sim (True) ou não (False)
# Existem vários operadores para questionar
# Dentre eles o ==, que é o operador lógico que 
# questiona se um valor é igual a outro

print( 10 == 10)  # Sim => True (verdadeiro)
print(10 == 1.0)  # Não => False (falso)
print(type(10 == 1))
print(type(10==10))
print(type(True))
print(type(False))